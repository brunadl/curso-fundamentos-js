// Iniciando sem valor - undefined
let variavelSemInicializacao
console.log(
    variavelSemInicializacao,
    typeof(variavelSemInicializacao)
)

// Atribuindo valor à variável anterior
variavelSemInicializacao = "Hello, World!"
console.log(
    variavelSemInicializacao,
    typeof(variavelSemInicializacao)
)

// Valor vazio, pelo menos no momento = null (boa prática, ao invés de undefined)
variavelSemInicializacao = null
console.log(
    variavelSemInicializacao,
    typeof(variavelSemInicializacao)
)


